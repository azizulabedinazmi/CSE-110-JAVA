package AZMI03.Lab05;

public class SpecialSymbols {
    public static void main(String[] args) {
        // Square Root (√)
        char squareRoot = '\u221A';
        System.out.println("Square Root: " + squareRoot);

        // Ohm (Ω)
        char ohmSymbol = '\u2126';
        System.out.println("Ohm (Ω) Symbol: " + ohmSymbol);

        // Plus Minus (±)
        char plusMinus = '\u00B1';
        System.out.println("Plus Minus (±) Symbol: " + plusMinus);

        // Not Equal (≠)
        char notEqual = '\u2260';
        System.out.println("Not Equal (≠) Symbol: " + notEqual);
    }
}